//this code will calculate the area of a circle with a radius of 5.6 cm

// making the variables
const PI: number = 3.14;
const radius: number = 5.6;

//calculation

const area: number = PI * radius * radius;

//display the results

console.log("The Radius is:", radius, "cm");
console.log("The area of the circle is:", area, "square cm")